namespace Skybrud.Social.Facebook.Models.Common.Tags {

    /// <summary>
    /// Enum class representing the type of a profile tag.
    /// </summary>
    public enum FacebookProfileTagType {

        /// <summary>
        /// Indictates that the type of the profile wasn't specified.
        /// </summary>
        Unspecified,

        /// <summary>
        /// Indicates that the type of the profile is a user.
        /// </summary>
        User,

        /// <summary>
        /// Indicates that the type of the profile is a page.
        /// </summary>
        Page,

        /// <summary>
        /// Indicates that the type of the profile is a group.
        /// </summary>
        Group

    }

}